#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, t_size_t n)
{
	t_size_t	i;

	i = -1;
	while (++i < n && (((unsigned char *)s1)[i] || ((unsigned char *)s2)[i]))
	{
		if (((unsigned char *)s1)[i] != ((unsigned char *)s2)[i])
			return (((unsigned char *)s1)[i] - ((unsigned char *)s2)[i]);
	}
	return (0);
}
